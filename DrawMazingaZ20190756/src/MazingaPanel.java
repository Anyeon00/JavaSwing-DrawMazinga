    public void drawClock(Graphics g, int clockX, int clockY, int clockR){
        Font tmpFont = g.getFont();
        setFont(tmpFont);
        Font clockFont = new Font("Sanserif", Font.BOLD, 23);
        g.setFont(clockFont);
        g.drawOval(clockX - clockR-30, clockY - clockR-25, clockR*2+50, clockR*2+50);
        double theta = 0;
        double step = (2*Math.PI)/12;
        int clockStringX = 0;
        int clockStringY = 0;
        theta += step*10;
        for (int i = 1; i <= 12; i++){
            clockStringX = (int) (clockX + clockR * Math.cos(theta))-11;
            clockStringY = (int) (clockY + clockR * Math.sin(theta))+11;
            String time = i + "";
            g.drawString(time, clockStringX, clockStringY);
            if (i == 1) {
                g.drawLine(clockX, clockY, clockStringX, clockStringY);
            }
            if (i == 8) {   //Math.abs(clockX-clockStringX)/2
                g.drawLine(clockX, clockY, clockStringX, clockStringY);
            }
            theta += step;
        }
        setFont(tmpFont);
    }

