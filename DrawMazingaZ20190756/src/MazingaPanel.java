    public void drawStar(Graphics g, int positionX, int positionY, int startAngleX) {
        int angleN = 5;
        double startAngle = startAngleX * (2*Math.PI/360);
        double bigTheta = startAngle;
        double smallTheta = (2*Math.PI)/angleN/2 + startAngle;
        double step = (2*Math.PI)/angleN;
        int bigR = 21;
        int littleR = 7;

        Polygon star = new Polygon();
        for (int i = 0; i < angleN; i++) {
            int bigStarX = (int) (bigR * Math.cos(bigTheta)) + positionX;
            int bigStarY = (int) (bigR * Math.sin(bigTheta)) + positionY;
            int smallStarX = (int) (littleR * Math.cos(smallTheta)) + positionX;
            int smallStarY = (int) (littleR * Math.sin(smallTheta)) + positionY;

            star.addPoint(bigStarX,bigStarY);
            star.addPoint(smallStarX,smallStarY);
            bigTheta += step;
            smallTheta += step;
        }
        g.drawPolygon(star);
        g.fillPolygon(star);
    }
